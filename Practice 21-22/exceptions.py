"""
Модуль пользовательских исключений для работы с матрицами.

Содержит иерархию специализированных исключений для обработки различных
типов ошибок, возникающих при выполнении матричных операций.

Иерархия исключений:
    MatrixError (базовое исключение)
    ├── NoDataError (отсутствуют данные для операции)
    ├── InvalidInputError (некорректный ввод пользователя)
    └── AlgorithmNotExecutedError (операция не выполнена)

Преимущества использования пользовательских исключений:
    1. Более точная классификация ошибок
    2. Упрощение отладки и обработки ошибок
    3. Улучшение читаемости кода
    4. Возможность централизованной обработки специфических ошибок
"""


class MatrixError(Exception):
    """
    Базовое исключение для всех ошибок, связанных с работой с матрицами.
    
    Это исключение служит родительским классом для всех специализированных
    исключений матричных операций. Использование базового класса позволяет
    перехватывать все матричные ошибки одной конструкцией try/except.
    
    Args:
        message: Текстовое описание ошибки
        *args: Дополнительные аргументы для базового класса Exception
    
    Example:
        >>> try:
        ...     # Код, который может вызвать матричную ошибку
        ... except MatrixError as e:
        ...     print(f"Ошибка матричной операции: {e}")
    
    Inheritance:
        Наследует все возможности стандартного класса Exception Python.
    """
    pass


class NoDataError(MatrixError):
    """
    Исключение для случаев, когда выбрано действие без введённых данных.
    
    Вызывается при попытке выполнить операцию с матрицей, когда матрица
    не была инициализирована (имеет значение None). Это предотвращает
    выполнение операций над несуществующими данными.
    
    Типичные сценарии возникновения:
        - Попытка повернуть матрицу до её ввода или генерации
        - Выполнение операций с неинициализированной матрицей
    
    Args:
        message: Сообщение об ошибке (обычно из словаря MESSAGES)
        *args: Дополнительные аргументы для базового класса
    
    Example:
        >>> data = None
        >>> if data is None:
        ...     raise NoDataError("Сначала введите или сгенерируйте матрицу!")
        NoDataError: Сначала введите или сгенерируйте матрицу!
    
    See Also:
        MESSAGES["no_data"] - стандартное сообщение для этой ошибки
    """
    pass


class InvalidInputError(MatrixError):
    """
    Исключение для случаев ввода некорректных значений пользователем.
    
    Вызывается при различных ошибках валидации пользовательского ввода:
        - Ввод нечисловых значений для размеров матрицы
        - Ввод отрицательных или нулевых размеров матрицы
        - Некорректное направление поворота матрицы
        - Другие ошибки формата вводимых данных
    
    Args:
        message: Детальное описание ошибки ввода
        *args: Дополнительные аргументы для базового класса
    
    Example:
        >>> direction = "invalid_direction"
        >>> if direction not in ['clockwise', 'counterclockwise']:
        ...     raise InvalidInputError("Некорректное направление поворота")
        InvalidInputError: Некорректное направление поворота
    
    Note:
        Это исключение также перехватывает ValueError при преобразовании
        строк в числа, предоставляя более информативное сообщение.
    """
    pass


class AlgorithmNotExecutedError(MatrixError):
    """
    Исключение для попытки вывода результата до выполнения алгоритма.
    
    Вызывается при попытке доступа к результату операции, когда операция
    не была выполнена (результат равен None). Это обеспечивает корректную
    последовательность выполнения операций.
    
    Типичные сценарии возникновения:
        - Попытка вывести результат поворота до выполнения поворота
        - Доступ к результату после сброса данных
        - Вывод результата при его отсутствии
    
    Args:
        message: Сообщение об ошибке (обычно из словаря MESSAGES)
        *args: Дополнительные аргументы для базового класса
    
    Example:
        >>> result = None
        >>> if result is None:
        ...     raise AlgorithmNotExecutedError("Сначала выполните алгоритм!")
        AlgorithmNotExecutedError: Сначала выполните алгоритм!
    
    See Also:
        MESSAGES["algorithm_not_executed"] - стандартное сообщение для этой ошибки
    """
    pass